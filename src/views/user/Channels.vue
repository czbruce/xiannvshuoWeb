<template>
  <div class="app-container">
    <div class="filter-container">
      渠道公司名称：
      <el-input v-model="listQuery.channelName" placeholder="用户名" style="width: 200px;" class="filter-item" @keyup.enter.native="handleFilter" />
      <!--      <el-select v-model="listQuery.sort" style="width: 140px" class="filter-item" @change="handleFilter">-->
      <!--        <el-option v-for="item in sortOptions" :key="item.key" :label="item.label" :value="item.key" />-->
      <!--      </el-select>-->
      <el-button v-waves class="filter-item" type="primary" icon="el-icon-search" @click="handleFilter">
        搜索
      </el-button>
      <el-button class="filter-item" style="margin-left: 10px;" type="primary" icon="el-icon-edit" @click="handleCreate">
        新增渠道
      </el-button>
    </div>

    <el-table
      :key="tableKey"
      v-loading="listLoading"
      :data="list"
      border
      fit
      highlight-current-row
      style="width: 100%;"
      @sort-change="sortChange"
    >
      <el-table-column label="ID" prop="id" sortable="custom" align="center" width="80" :class-name="getSortClass('id')">
        <template slot-scope="{row}">
          <span>{{ row.id }}</span>
        </template>
      </el-table-column>
      <el-table-column label="渠道id" width="150px" align="center">
        <template slot-scope="{row}">
          <span>{{ row.channelId }}</span>
        </template>
      </el-table-column>
      <el-table-column label="渠道公司" min-width="200px" align="center">
        <template slot-scope="{row}">
          <span>{{ row.channelName }}</span>
        </template>
      </el-table-column>
      <el-table-column label="空白二维码数量" width="150px" align="center">
        <template slot-scope="{row}">
          <span>{{ row.privateCode }}</span>
        </template>
      </el-table-column>

      <el-table-column label="激活二维码量" width="150px" align="center">
        <template slot-scope="{row}">
          <span class="link-type" @click="handleUpdate(row)">{{ row.blankQrCode }}</span>
        </template>
      </el-table-column>

      <el-table-column label="Actions" align="center" width="230" class-name="small-padding fixed-width">
        <template slot-scope="{row}">
          <!--          <el-button type="primary" size="mini" @click="handleUpdate(row)">-->
          <!--            详情-->
          <!--          </el-button>-->
          <el-button type="primary" size="" @click="handleOrderList(row)">
            二维码列表
          </el-button>
          <el-button v-if="row.status!='deleted'" size="mini" type="danger" @click="handleDelete(row,$index)">
            删除
          </el-button>
        </template>
      </el-table-column>
    </el-table>

    <pagination v-show="total>0" :total="total" :page.sync="listQuery.page" :limit.sync="listQuery.limit" @pagination="getList" />

    <el-dialog :title="textMap[dialogStatus]" :visible.sync="dialogFormVisible">
      <el-form ref="dataForm" :rules="rules" :model="temp" label-position="left" label-width="85px" style="width: 400px; margin-left:50px;">
        <el-form-item label="所属渠道名称" prop="channelName">
          <el-input v-model="temp.channelName" />
        </el-form-item>
        <el-form-item label="空白二维码数量" prop="author">
          <el-input v-model="temp.privateCode" />
        </el-form-item>
        <el-form-item label="公司类型" prop="author">
          <el-select v-model="cooperation" style="width: 140px;margin-right: 10px;" class="filter-item" @change="handleFilter">
            <el-option v-for="item in cooperation" :key="item.cooperationInd" :label="item.name" :value="item.cooperationInd" />
          </el-select>
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button @click="dialogFormVisible = false">
          返回
        </el-button>
        <el-button type="primary" @click="dialogStatus==='create'?createData():updateData()">
          确定
        </el-button>
      </div>
    </el-dialog>

    <el-dialog :visible.sync="dialogPvVisible" title="Reading statistics">
      <el-table :data="pvData" border fit highlight-current-row style="width: 100%">
        <el-table-column prop="key" label="Channel" />
        <el-table-column prop="pv" label="Pv" />
      </el-table>
      <span slot="footer" class="dialog-footer">
        <el-button type="primary" @click="dialogPvVisible = false">Confirm</el-button>
      </span>
    </el-dialog>

    <el-dialog v-el-drag-dialog :visible.sync="dialogTableVisible" title="二维码列表" @dragDialog="handleDrag">
      <el-select ref="select" v-model="value" placeholder="请选择">
        <el-option v-for="item in options" :key="item.value" :label="item.label" :value="item.value" />
      </el-select>
      <el-table :data="gridData">
        <el-table-column property="date" label="Date" width="150" />
        <el-table-column property="name" label="Name" width="200" />
        <el-table-column property="address" label="Address" />
      </el-table>
    </el-dialog>

  </div>
